package com.ggwaradzimba;

public class Lettuce {
    private String name;
    private double price;

    public Lettuce() {
        this.name = "lettuce";
        this.price = 2.50;
    }

    public String getName() {
        return this.name;
    }

    public double getPrice() {
        return this.price;
    }
}
